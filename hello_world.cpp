#include <iostream> //header file library that lets us work with input and output objects and adds functionality to C++ programs
using namespace std; //allow us to use names for objects and variables from standard library
//whitespace for readable code; ignored by C++
int main() { //function that always appears in C++
    cout << "Hello World!"; //object used together with the insertion operator output text
    return 0; //closes the main function
} //ends the main function

/*
cout == input/output object (pronounced as "see-out")
<< == insertion operator
std::cout == object we'd use if we omitted "using namespace std"
*/